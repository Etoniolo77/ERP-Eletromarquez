-- 1. Produtividade
CREATE TABLE public.produtividade (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    equipe TEXT,
    tipo_equipe TEXT,
    setor TEXT,
    data DATE,
    csd TEXT,
    ocupacao DOUBLE PRECISION,
    produtividade_pct DOUBLE PRECISION,
    eficiencia_pct DOUBLE PRECISION,
    eficacia_pct DOUBLE PRECISION,
    notas_executadas DOUBLE PRECISION,
    notas_interrompidas DOUBLE PRECISION DEFAULT 0.0,
    notas_rejeitadas DOUBLE PRECISION DEFAULT 0.0,
    ociosidade_min DOUBLE PRECISION,
    desvios_min DOUBLE PRECISION,
    deslocamento_min DOUBLE PRECISION,
    hhp_min DOUBLE PRECISION,
    hhr_min DOUBLE PRECISION DEFAULT 0.0,
    saida_base_min DOUBLE PRECISION DEFAULT 0.0,
    retorno_base_min DOUBLE PRECISION DEFAULT 0.0,
    hora_extra_min DOUBLE PRECISION DEFAULT 0.0
);

-- Índices Recomendados para Produtividade
CREATE INDEX idx_produtividade_equipe ON public.produtividade(equipe);
CREATE INDEX idx_produtividade_setor ON public.produtividade(setor);
CREATE INDEX idx_produtividade_data ON public.produtividade(data);

