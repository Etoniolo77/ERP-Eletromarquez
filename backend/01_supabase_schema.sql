-- ==========================================
-- POLÍTICAS DE SEGURANÇA (RLS - Row Level Security)
-- Habilitando RLS para todas as tabelas, mas permitindo leitura pública inicial
-- ==========================================

DO $$
DECLARE
    t_name text;
BEGIN
    FOR t_name IN (SELECT tablename FROM pg_tables WHERE schemaname = 'public' AND tablename IN ('produtividade', 'auditorias_5s', 'frota_custos', 'indisponibilidade', 'sync_logs', 'rejeicoes', 'logccm_mb52', 'logccm_item', 'logccm_ruptura', 'logccm_serial', 'apr_records', 'saida_base_records', 'system_configs')) LOOP
        EXECUTE 'ALTER TABLE public.' || quote_ident(t_name) || ' ENABLE ROW LEVEL SECURITY;';
        -- Criando política genérica de SELECT (Apenas usuários logados)
        EXECUTE 'CREATE POLICY ' || quote_ident('permitir_leitura_auth_' || t_name) || ' ON public.' || quote_ident(t_name) || ' FOR SELECT TO authenticated USING (true);';
        -- Service Role (Robô local usando url/key) tem ALL access bypassando RLS (Padrão no Supabase com Service Key)
    END LOOP;
END
$$;

